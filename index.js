function isDateInSeason(month, day, seasonObject) {
  /* Check if the 0 indexed month and 1 indexed day are within a season */

  // Make 1 indexed to match date representation
  month = month + 1

  // If the season rolls across the new year boundary, add twelve to the
  // lookup month and end month, should keep calculations correct
  if (seasonObject["seasonStartMonth"] > seasonObject["seasonEndMonth"]) {
    seasonObject = JSON.parse(JSON.stringify(seasonObject))
    seasonObject["seasonEndMonth"] += 12
    month += 12
  }
  var greaterThanStartDate = month > seasonObject["seasonStartMonth"] ||
      (month === seasonObject["seasonStartMonth"] &&
      day >= seasonObject["seasonStartDay"])

  var lessThanEndDate = month < seasonObject["seasonEndMonth"] ||
    (month === seasonObject["seasonEndMonth"] &&
    day <= seasonObject["seasonEndDay"])

  return greaterThanStartDate && lessThanEndDate
}

